using MailKit.Net.Smtp;
using MimeKit;
using System;
using System.Collections.Generic;
using System.Text;

namespace ServiceLayer.SendMessage
{
    public class SendMessage
    {
        public void Send(string firstName, string secondName, string firstLastName, string secondLastName, string email)
        {
            var message = new MimeMessage();
            message.From.Add(new MailboxAddress("reymanproject@gmail.com"));
            message.To.Add(new MailboxAddress("juanda.mdc97@gmail.com"));
            message.Subject = "Nuevo usuario";
            message.Body = new TextPart("plain")
            {
                Text = $"El usuario {firstName + secondLastName + firstLastName + secondLastName}, con email {email}, esta pendiente de autorizacion."
            };

            using (var client = new SmtpClient())
            {
                client.Connect("localhost", 587, false);
                //client.Authenticate("reymanproject@gmail.com", "REYMAN.2019");
                client.Send(message);
                client.Disconnect(true);
            }
        }
    }
}
